    private void calculateActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_calculateActionPerformed
    {//GEN-HEADEREND:event_calculateActionPerformed
          if(this.x.getText().equals("") && this.y.getText().equals(""))
          {
              this.Answer.setText("");
          }  
          else 
          {
              int p=Integer.parseInt(this.x.getText()); 
              int q=Integer.parseInt(this.y.getText()); 
               
              this.Answer.setText("Area: "+Integer.toString(p*q)+" perimeter: "+Integer.toString(2*(p+q)));
          }
          
    }//GEN-LAST:event_calculateActionPerformed

