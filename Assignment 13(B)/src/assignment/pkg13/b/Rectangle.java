    private void xKeyTyped(java.awt.event.KeyEvent evt)//GEN-FIRST:event_xKeyTyped
    {//GEN-HEADEREND:event_xKeyTyped
          char ch;
          ch=evt.getKeyChar(); 
           
          if(Character.isDigit(ch)==false)
          {
              evt.consume();
          }
          
    }//GEN-LAST:event_xKeyTyped

